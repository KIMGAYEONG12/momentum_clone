// 🎯 미션: 아래 조건을 모두 만족하는 타이머를 완성하세요!
//
//  1. 변수 remain을 10으로 설정
//  2. 매 1초마다 remain을 1씩 줄이며 현재 값을 출력
//     예) "⏱ 남은 시간: 10초" ~ "⏱ 남은 시간: 1초"
//  3. remain이 0이 되면 인터벌을 멈추고 "⏰ 시간 종료!" 출력
//  4. 단, remain이 3 이하일 때는 "⚠️ 얼마 안 남았다!" 도 함께 출력

let remain = 10;

let missionTimer = setInterval(() => {
  // 👇 여기에 전체 로직을 작성하세요
  console.log(`⏱ 남은 시간: ${remain}초`);

  // 3초 이하 경고
  if (remain <= 3 && remain > 0) {
    console.log("⚠️ 얼마 안 남았다!");
  }

  remain--;

  // 종료 조건
  if (remain === 0) {
    console.log("⏱ 남은 시간: 0초");
    console.log("⏰ 시간 종료!");
    clearInterval(missionTimer);
  }
}, 1000);

// 기대 출력 (예시):
// ⏱ 남은 시간: 10초
// ⏱ 남은 시간: 9초
// ...
// ⏱ 남은 시간: 3초
// ⚠️ 얼마 안 남았다!
// ⏱ 남은 시간: 2초
// ⚠️ 얼마 안 남았다!
// ⏱ 남은 시간: 1초
// ⚠️ 얼마 안 남았다!
// ⏰ 시간 종료!
