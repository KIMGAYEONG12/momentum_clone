// Q2-1. 아래 타이머를 즉시 취소하여 메시지가 출력되지 않게 하세요.
let cancelMe = setTimeout(() => {
  console.log("이 메시지는 출력되면 안 됩니다!");
}, 2000);

// 👇 여기에 타이머를 취소하는 코드를 작성하세요
clearTimeout(cancelMe);

// 기대 출력: (아무것도 출력되지 않음)

// Q2-2. 빈칸을 채워 타이머 ID를 저장하고 취소하세요.
let timerId = setTimeout(() => {
  console.log("취소될 메시지");
}, 1000);

