// Q1-1. 빈칸을 채워 1.5초 뒤에 메시지를 출력하세요.
setTimeout(function () {
  console.log("1.5초 뒤에 실행!"); // 기대 출력: 1.5초 뒤에 실행!
}, 1500);

// Q1-2. 화살표 함수로 3초 뒤에 이름을 출력하세요.
const myName = "홍길동";
setTimeout(() => {
  console.log(`안녕하세요, ${myName}님!`);
}, 3000);
// 기대 출력: 안녕하세요, 홍길동님!

// Q1-3. 아래 코드의 출력 순서를 주석으로 예측해보세요.
console.log("A");
setTimeout(() => {
  console.log("B");
}, 0);
console.log("C");
// 예상 출력 순서: ① A ② C ③ B
// (힌트: setTimeout은 시간이 0이어도 비동기로 처리됩니다!)

