// Q5-1. setTimeout 재귀 패턴으로 1초마다 "똑딱" 3번 출력하세요.
let tickCount = 0;

const tick = () => {
  tickCount++;
  console.log(`똑딱 ${tickCount}`); // 기대 출력: 똑딱 1 / 똑딱 2 / 똑딱 3

  // 👇 tickCount가 3보다 작을 때만 재귀 호출하는 코드를 작성하세요 (삼항 연산자)
  tickCount < 3 ? setTimeout(tick, 1000) : null;
};
setTimeout(tick, 1000); // 첫 호출 시작

// Q5-2. 아래 설명에 맞는 것을 고르세요.
//       A: setInterval   B: setTimeout 재귀
//
//  (가) 함수 실행 완료 후 다음 예약을 걸기 때문에 겹치지 않는다 → B
//  (나) 고정된 간격으로 실행되므로 함수가 오래 걸리면 겹칠 수 있다 → A
//  (다) 조건에 따라 실행 횟수를 유연하게 조정하기 쉽다 → B

// ════════════════════════════════
// ── Q6단계: 종합 도전 — 나만의 타이머 만들기 ──
