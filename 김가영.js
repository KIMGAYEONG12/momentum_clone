// Q4-1. 카운트가 5가 되면 인터벌을 멈추도록 완성하세요.
let num = 0;
let autoStop = setInterval(() => {
  num++;
  console.log(`카운트: ${num}`); // 기대 출력: 카운트: 1 ~ 카운트: 5

  // 👇 num이 5가 되면 autoStop을 멈추는 코드를 작성하세요 (삼항 연산자 사용)
  num === 5 ? (clearInterval(autoStop), console.log("완료!")) : null;
  //                                                  // 기대 출력: 완료!
}, 500);

// Q4-2. 빈칸을 채워 매 2초마다 랜덤 숫자를 출력하고,
//       숫자가 7 이상이면 멈추도록 완성하세요.
let luckyTimer = setInterval(() => {
  let random = Math.floor(Math.random() * 10) + 1; // 1~10 랜덤 숫자
  console.log(`뽑은 숫자: ${random}`); // 기대 출력: 뽑은 숫자: (1~10 랜덤)

  // 👇 여기에 조건을 완성하세요
  random >= 7
    ? (clearInterval(luckyTimer),
      console.log(`🎉 ${random} 나왔다! 인터벌 종료`))
    : null;
}, 2000);

// ════════════════════════════════
// ── Q5단계: setTimeout vs setInterval 비교 ──
