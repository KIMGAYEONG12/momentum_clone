// Q3-1. 빈칸을 채워 매 1초마다 "안녕!"을 출력하세요.
// (5초 뒤 자동으로 멈추는 코드도 완성하세요)
let helloTimer = setInterval(() => {
  console.log("안녕!"); // 기대 출력: 안녕! (매 1초, 5번)
}, 1000);

setTimeout(() => {
  clearInterval(helloTimer); // 인터벌 멈추기
  console.log("인사 종료!"); // 기대 출력: 인사 종료!
}, 5000);

// Q3-2. 아래 코드에서 잘못된 부분을 찾아 고치세요.
// (인터벌을 멈추려면 clearInterval이 필요합니다)
let buggyTimer = setInterval(() => {
  console.log("버그 있는 타이머");
}, 1000);

// ❌ 아래 코드가 잘못되었습니다. 올바르게 수정하세요.
clearInterval(buggyTimer); // 👈 이 줄을 수정하세요
// 기대 결과: 인터벌이 정상적으로 멈춰야 합니다.

// ════════════════════════════════
// ── Q4단계: clearInterval로 반복 중지 ──
